#!/usr/bin/python3

str = "Holberton School"
# Initialise la variable 'str' avec la chaîne de caractères "Holberton School"

print(str * 3)
# Affiche la chaîne 'str' concaténée 3 fois à la suite (sans espace entre chaque répétition)
# Résultat affiché : "Holberton SchoolHolberton SchoolHolberton School"

print(str[:9])
# Affiche les 9 premiers caractères de la chaîne 'str' (du début jusqu'à l'indice 9 non inclus)
# Indices en Python : 0 = 'H', 1 = 'o', ..., 8 = 'n' (le 9ème caractère, ' ', n'est pas inclus)
# Résultat affiché : "Holberton"
