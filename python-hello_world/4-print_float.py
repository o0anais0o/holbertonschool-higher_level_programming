#!/usr/bin/python3
number = 3.14159
# Initialise la variable 'number' avec la valeur décimale 3.14159 (type float)
print(f"Float: {number:.2f}")
# Utilise une f-string (format string) pour afficher la valeur de 'number'
# Le formatage ": .2f" signifie : afficher le nombre avec 2 chiffres après la virgule (arrondi si nécessaire)
# Le résultat affiché sera : "Float: 3.14"
